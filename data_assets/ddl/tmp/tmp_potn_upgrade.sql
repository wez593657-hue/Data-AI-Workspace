CREATE TABLE IF NOT EXISTS TMP_POTN_DTL_BASE (
    PERSN_LEGAL_BK_CODE VARCHAR(4),
    CUST_ID VARCHAR(20) NOT NULL,
    CUST_NAME VARCHAR(100),
    CUST_LVL VARCHAR(2),
    LVL_CRIT VARCHAR(2) NOT NULL,
    DEPO_CURNT_DEPO_BAL NUMBER(20,
    2) NOT NULL,
    FIXD_DEPO_BAL NUMBER(20,
    2) NOT NULL,
    FIN_AMT NUMBER(20,
    2) NOT NULL,
    CURR_MTH_AVG_AUM NUMBER(20,
    2) NOT NULL,
    PNT_AUM_BAL NUMBER(20,
    2) NOT NULL,
    CNTCT_STATE VARCHAR(1) NOT NULL,
    POST_ID VARCHAR(20),
    ORG_ID VARCHAR(7)
);
CREATE TABLE IF NOT EXISTS TMP_POTN_STAT_BASE (    PERSN_LEGAL_BK_CODE VARCHAR(4), CUST_ID VARCHAR(20) NOT NULL, LVL_CRIT VARCHAR(2) NOT NULL,    MTH_AVG_QUAL_STATE VARCHAR(1) NOT NULL, PNT_QUAL_STATE VARCHAR(1) NOT NULL, CNTCT_STATE VARCHAR(1) NOT NULL, POST_ID VARCHAR(20), ORG_ID VARCHAR(7));
CREATE TABLE IF NOT EXISTS TMP_POTN_STAT_SRC (    PERSN_LEGAL_BK_CODE VARCHAR(4), DATA_DATE VARCHAR(8) NOT NULL, STATIS_CYCLE VARCHAR(2) NOT NULL,    STATIS_OBJ VARCHAR(20) NOT NULL, LVL_CRIT VARCHAR(2) NOT NULL,    MTH_AVG_QUAL_STATE VARCHAR(1) NOT NULL, PNT_QUAL_STATE VARCHAR(1) NOT NULL, CNTCT_STATE VARCHAR(1) NOT NULL,);
ALTER TABLE TMP_POTN_STAT_SRC ADD COLUMN IF NOT EXISTS DATA_DATE VARCHAR(8);
ALTER TABLE TMP_POTN_STAT_SRC ADD COLUMN IF NOT EXISTS STATIS_CYCLE VARCHAR(2);
COMMENT ON TABLE TMP_POTN_DTL_BASE IS '潜力提升明细基础临时表';
COMMENT ON TABLE TMP_POTN_STAT_BASE IS '潜力提升统计基础临时表';
COMMENT ON TABLE TMP_POTN_STAT_SRC IS '潜力提升统计对象临时表';
COMMENT ON COLUMN TMP_POTN_DTL_BASE.LVL_CRIT IS '目标临界等级：03优质，04财富1，05财富2，06贵宾，07私行';
COMMENT ON COLUMN TMP_POTN_DTL_BASE.CURR_MTH_AVG_AUM IS '数据日期月日均AUM';
COMMENT ON COLUMN TMP_POTN_DTL_BASE.PNT_AUM_BAL IS 'T-1日时点AUM余额';
COMMENT ON COLUMN TMP_POTN_DTL_BASE.CNTCT_STATE IS '当月接触状态：0否，1是';
COMMENT ON COLUMN TMP_POTN_STAT_BASE.MTH_AVG_QUAL_STATE IS '月均达标状态：0否，1是';
COMMENT ON COLUMN TMP_POTN_STAT_BASE.PNT_QUAL_STATE IS '时点达标状态：0否，1是';
COMMENT ON COLUMN TMP_POTN_STAT_SRC.STATIS_OBJ IS '统计对象：机构号或客户经理职位编号';
CREATE INDEX IF NOT EXISTS IDX_TMP_POTN_DTL_BASE_01 ON TMP_POTN_DTL_BASE (CUST_ID, LVL_CRIT);
CREATE INDEX IF NOT EXISTS IDX_TMP_POTN_STAT_BASE_01 ON TMP_POTN_STAT_BASE (ORG_ID, POST_ID, LVL_CRIT);
CREATE INDEX IF NOT EXISTS IDX_TMP_POTN_STAT_SRC_01 ON TMP_POTN_STAT_SRC (STATIS_OBJ, LVL_CRIT);
