CREATE TABLE IF NOT EXISTS TMP_CDR_STAT_BASE (
    PERSN_LEGAL_BK_CODE VARCHAR(4),
    DATA_DATE VARCHAR(8) NOT NULL,
    STAT_PERD VARCHAR(2) NOT NULL,
    STATIS_TYP VARCHAR(2) NOT NULL,
    CUST_ID VARCHAR(20) NOT NULL,
    ORG_ID VARCHAR(7),
    POST_ID VARCHAR(20),
    EXPR_AMT NUMBER(20,
    2) NOT NULL,
    MATURE_TTL_AMT NUMBER(20,
    2) NOT NULL,
    TAKE_RATE_30D NUMBER(10,
    2) NOT NULL,
    CUST_TAKE_FLG VARCHAR(1) NOT NULL,
    FIXED_MATURE_TRAN_FIN_AMT NUMBER(20,
    2) NOT NULL,
    FIXED_FIN_MATURE_TRAN_INSUR_AMT NUMBER(20,
    2) NOT NULL,
    FIN_MATURE_TRAN_FIXED_AMT NUMBER(20,
    2) NOT NULL,
    FRST_MATURE_PK_BF_DAY_AUM_BAL NUMBER(20,
    2) NOT NULL,
    CURR_AUM_BAL NUMBER(20,
    2) NOT NULL
);

CREATE TABLE IF NOT EXISTS TMP_CDR_STAT_SRC (    PERSN_LEGAL_BK_CODE VARCHAR(4),    STATIS_OBJ VARCHAR(20) NOT NULL,    DATA_DATE VARCHAR(8) NOT NULL,    STAT_PERD VARCHAR(2) NOT NULL,    STATIS_TYP VARCHAR(2) NOT NULL,    CUST_ID VARCHAR(20) NOT NULL,    ORG_ID VARCHAR(7),    POST_ID VARCHAR(20),    EXPR_AMT NUMBER(20,2) NOT NULL,    MATURE_TTL_AMT NUMBER(20,2) NOT NULL,    TAKE_RATE_30D NUMBER(10,2) NOT NULL,    CUST_TAKE_FLG VARCHAR(1) NOT NULL,    FIXED_MATURE_TRAN_FIN_AMT NUMBER(20,2) NOT NULL,    FIXED_FIN_MATURE_TRAN_INSUR_AMT NUMBER(20,2) NOT NULL,    FIN_MATURE_TRAN_FIXED_AMT NUMBER(20,2) NOT NULL,    FRST_MATURE_PK_BF_DAY_AUM_BAL NUMBER(20,2) NOT NULL,    CURR_AUM_BAL NUMBER(20,2) NOT NULL,);

