ALTER TABLE TMP_LOST_STAT_SRC ADD COLUMN IF NOT EXISTS DATA_DATE VARCHAR(8);
ALTER TABLE TMP_LOST_STAT_SRC ADD COLUMN IF NOT EXISTS STATIS_CYCLE VARCHAR(2);
COMMENT ON TABLE TMP_LOST_STAT_SRC IS '流失挽回统计对象临时表';
