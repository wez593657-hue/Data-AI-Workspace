CREATE TABLE IF NOT EXISTS TMP_LOST_DTL_BASE (
    PERSN_LEGAL_BK_CODE VARCHAR(4),
    CUST_ID VARCHAR(20) NOT NULL,
    CUST_NAME VARCHAR(100),
    CUST_LVL VARCHAR(2),
    LVL_CHURN VARCHAR(1) NOT NULL,
    DEPO_CURNT_DEPO_BAL NUMBER(20,
    2) NOT NULL,
    FIXD_DEPO_BAL NUMBER(20,
    2) NOT NULL,
    FIN_AMT NUMBER(20,
    2) NOT NULL,
    CNTCT_STATE VARCHAR(1) NOT NULL,
    RESCUE_STATE VARCHAR(1) NOT NULL,
    POST_ID VARCHAR(20),
    ORG_ID VARCHAR(7)
);
COMMENT ON TABLE TMP_LOST_DTL_BASE IS '流失挽回明细基础临时表';
COMMENT ON COLUMN TMP_LOST_DTL_BASE.LVL_CHURN IS '流失等级：1轻度，2重度';
COMMENT ON COLUMN TMP_LOST_DTL_BASE.RESCUE_STATE IS 'T-1日AUM达到原等级门槛：0否，1是';
CREATE INDEX IF NOT EXISTS IDX_TMP_LOST_DTL_BASE_01 ON TMP_LOST_DTL_BASE(ORG_ID,POST_ID,LVL_CHURN);
