    FIN_ESTIM_RSLT VARCHAR(100) NULL,
    FIN_SCORE VARCHAR(100) NULL,
    FIN_RISK_LVL VARCHAR(100) NULL,
    FIN_ESTIM_DATE VARCHAR(100) NULL,
    FIN_EXPR_DATE VARCHAR(100) NULL,
    INS_INVEST_TYP VARCHAR(100) NULL,
    INS_ESTIM_RSLT VARCHAR(100) NULL,
    INS_SCORE VARCHAR(100) NULL,
    INS_RISK_LVL VARCHAR(100) NULL,
    INS_ESTIM_DATE VARCHAR(100) NULL,
    INS_EXPR_DATE VARCHAR(100) NULL,
    PERSN_LEGAL_BK_CODE VARCHAR(100) NULL
);

