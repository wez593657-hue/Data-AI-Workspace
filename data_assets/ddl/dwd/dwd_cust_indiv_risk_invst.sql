COMMENT ON COLUMN DWD_CUST_INDIV_RISK_INVST.FIN_ESTIM_RSLT IS '理财评估结果';
COMMENT ON COLUMN DWD_CUST_INDIV_RISK_INVST.FIN_SCORE IS '理财分数';
COMMENT ON COLUMN DWD_CUST_INDIV_RISK_INVST.FIN_RISK_LVL IS '理财风险级别';
COMMENT ON COLUMN DWD_CUST_INDIV_RISK_INVST.FIN_ESTIM_DATE IS '理财评估日期';
COMMENT ON COLUMN DWD_CUST_INDIV_RISK_INVST.FIN_EXPR_DATE IS '理财到期日期';
COMMENT ON COLUMN DWD_CUST_INDIV_RISK_INVST.INS_INVEST_TYP IS '保险投资类型';
COMMENT ON COLUMN DWD_CUST_INDIV_RISK_INVST.INS_ESTIM_RSLT IS '保险评估结果';
COMMENT ON COLUMN DWD_CUST_INDIV_RISK_INVST.INS_SCORE IS '保险分数';
COMMENT ON COLUMN DWD_CUST_INDIV_RISK_INVST.INS_RISK_LVL IS '保险风险级别';
