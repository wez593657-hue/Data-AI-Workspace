CREATE TABLE IF NOT EXISTS DWD_CUST_INDIV_RISK_INVST (
    CUST_ID VARCHAR(21) NOT NULL,
    INVEST_TYP VARCHAR(6) NULL,
    ESTIM_RSLT VARCHAR(6) NULL,
    SCORE NUMBER(22) NULL,
    RISK_LVL VARCHAR(6) NULL,
    ESTIM_DATE VARCHAR(10) NULL,
    EXPR_DATE VARCHAR(10) NULL,
    PERSN_LEGAL_BK_CODE VARCHAR(30) NULL
);

COMMENT ON TABLE DWD_CUST_INDIV_RISK_INVST IS '客户风险评估';
COMMENT ON COLUMN DWD_CUST_INDIV_RISK_INVST.CUST_ID IS '客户编号';
COMMENT ON COLUMN DWD_CUST_INDIV_RISK_INVST.INVEST_TYP IS '投资类型';
COMMENT ON COLUMN DWD_CUST_INDIV_RISK_INVST.ESTIM_RSLT IS '评估结果';
COMMENT ON COLUMN DWD_CUST_INDIV_RISK_INVST.SCORE IS '分数';
COMMENT ON COLUMN DWD_CUST_INDIV_RISK_INVST.RISK_LVL IS '风险级别';
COMMENT ON COLUMN DWD_CUST_INDIV_RISK_INVST.ESTIM_DATE IS '评估日期';
COMMENT ON COLUMN DWD_CUST_INDIV_RISK_INVST.EXPR_DATE IS '到期日期';
COMMENT ON COLUMN DWD_CUST_INDIV_RISK_INVST.PERSN_LEGAL_BK_CODE IS '法人行号';
