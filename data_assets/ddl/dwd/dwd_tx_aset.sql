COMMENT ON COLUMN DWD_TX_ASET.OPNT_BK_KEEP IS '对方行';
COMMENT ON COLUMN DWD_TX_ASET.OPNT_NAME_BK IS '对方行名';
COMMENT ON COLUMN DWD_TX_ASET.FEE_HAND IS '手续费';
COMMENT ON COLUMN DWD_TX_ASET.ACCT_BLNG_ORG IS '账户归属机构';
